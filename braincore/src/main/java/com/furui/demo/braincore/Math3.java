package com.furui.demo.braincore;

/**
 * @author furui
 *
 */
public class Math3 {
    /**
     * addition of two doubles
     * @param a
     * @param b
     * @return
     */
    public static double add(final double a, final double b) {
        return a + b;
    }

    /**
     * the multiply of two doubles
     * @param a
     * @param b
     * @return
     */
    public static double multiply(final double a, final double b) {
        return a * b;
    }
}
